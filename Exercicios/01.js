// // 1. Variáveis e Tipos**  
// Crie três variáveis diferentes: uma string, um number e um boolean.  
// Mostre no console o tipo de cada uma usando `typeof`

let number = 50;
let string = "Olá, mundo!";
let boolean = true;
let array = [1, 2, 3];

console.log(number);
console.log(typeof number + "\n"); // number
console.log(string);
console.log(typeof string + "\n"); // string  
console.log(boolean);
console.log(typeof boolean + "\n"); // boolean
console.log(array);
console.log(typeof array + "\n"); // object (arrays são considerados objetos em JavaScript)


